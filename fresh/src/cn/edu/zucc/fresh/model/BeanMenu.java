package cn.edu.zucc.fresh.model;

public class BeanMenu {
	private int menu_id;
	private String menu_name;
	private String menu_make;
	private String menu_step;
	public int getMenu_id() {
		return menu_id;
	}
	public void setMenu_id(int menu_id) {
		this.menu_id = menu_id;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public String getMenu_make() {
		return menu_make;
	}
	public void setMenu_make(String menu_make) {
		this.menu_make = menu_make;
	}
	public String getMenu_step() {
		return menu_step;
	}
	public void setMenu_step(String menu_step) {
		this.menu_step = menu_step;
	}
	
	
}
